let appleCost = 0;

function applePlusBtn() {
   appleCost += 10;
   document.querySelector("#price-btn1").innerHTML = appleCost;       

}

function appleMinusBtn() {
    appleCost -= 10;
    document.querySelector("#price-btn1").innerHTML = appleCost;       
 
 
 }


